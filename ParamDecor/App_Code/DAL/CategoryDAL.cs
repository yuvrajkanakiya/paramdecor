        #region SelectByPK
        public CategoryENT SelectByPK(SqlInt32 CategoryID)
        {
            using (SqlConnection objConn = new SqlConnection(ConnectionString))
            {
                objConn.Open();

                using (SqlCommand objCmd = objConn.CreateCommand())
                {
                    try
